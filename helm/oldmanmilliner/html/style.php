						<h3>Fedora</h3>
						<p>The superstar of the classic hat world, the Fedora rose to popularity during the Prohibition era of the United States where it earned a reputation for being a "gangsters" hat. This led to the Fedora’s golden age between the 1920s and the early 1950s. It remains the most iconic hat style in the western world. The fedora's popularity led to manufacturers to place the Fedora label on any hat that even resembling one. For the purest, a Fedora is a felt hat with pinched sides and a lengthwise crease down the crown giving the front a roughly wedge-like shape.</p>
						
