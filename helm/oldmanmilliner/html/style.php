						<h3>Porkpie</h3>
						<p>The porkpie resembles an actual pork pie. During the Great Depression, the likes of Buster Keaton and Jazz musicians such as Lester Young made the hat fashionable. The Porkpie's features include flattened crown without a crease down the center and pinched sides like a Fedora. The brim is usually small, and turned up all around.</p>
						
						<img src='images/charlieChaplinBowler.jpg'>

						<h3>Bowler</h3>
						<p>Designed by London hat makers Thomas and William Bowler, no one is quite certain who commissioned the Bowler. Famous actors such as Charlie Chaplin and John Cleese made and keep the hat in vogue.  It even remains a part of the formal uniform of British law enforcement.</p>
						
						<img src='images/princePhilipFlatCap.jpg'> 

						<h3>Flat Caps</h3>
						<p>Probably the most informal hat on this list, the Flat cap goes back to the 14th century and originated in Scotland where, originally, they called it a “bonnet” (and many still do). The flat cap's lengthy history continues full-steam across the western world and remains quite popular in US Cities. With a round rounded shape, a small brim, and a high back, it too possesses a wedge shape, but turned horizontal where the body of the hat comes foward and attaches to the front, or near the front, of the baseball-cap-like brim..</p>

