						<h3>Trilby</h3>
						<p>The name Trilby came from the stage adaptation of George du Maurier's 1894 novel <i>Trilby</i> in which the cast wore them in its first London production. The Trilby reached the peak of its popularity in the 1960s. A Trilby resembles a Fedora but is a little shorter and more angular. They share crown shapes except the Trilby's crown angled to the back.</p>
						
						<img src='images/churchillHomburg.jpg'>	
						
						<h3>Homburg</h3>	
						<p>All business all the time, Brits first fell in love with the Homburg when, in the 1890s, Edward the VII brought it back from Hesse, Germany’s Bad Homburg district. At the 1953 inauguration, President Dwight D. Eisenhower broke with tradition wearing a Homburg instead of a top hat. He gave the Homburg a repeat performance at his second inauguration that took 3 months to make and was crafted by people in 10 separate countries (some calling it the "International Homburg"). The Homburg possesses the same creased crown as a Fedora but without the pinch at the sides. The Homburg brim is stiffer and slightly upturned lip all the way around. Many still see it as the go-to dress hat of the businessman, the politician and other "well-to-do" gentlemen in the western world.</p>
						
